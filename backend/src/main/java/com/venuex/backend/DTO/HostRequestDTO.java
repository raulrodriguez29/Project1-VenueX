package com.venuex.backend.DTO;

import java.time.LocalDateTime;

public class HostRequestDTO {

    private Integer id;
    private Integer userId;
    private String status;
    private LocalDateTime requestedAt;
    private Integer reviewedBy;

    public HostRequestDTO() {
    }

    public HostRequestDTO(Integer id, LocalDateTime requestedAt, Integer reviewedBy, String status, Integer userId) {
        this.id = id;
        this.requestedAt = requestedAt;
        this.reviewedBy = reviewedBy;
        this.status = status;
        this.userId = userId;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public LocalDateTime getRequestedAt() {
        return requestedAt;
    }

    public void setRequestedAt(LocalDateTime requestedAt) {
        this.requestedAt = requestedAt;
    }

    public Integer getReviewedBy() {
        return reviewedBy;
    }

    public void setReviewedBy(Integer reviewedBy) {
        this.reviewedBy = reviewedBy;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        result = prime * result + ((userId == null) ? 0 : userId.hashCode());
        result = prime * result + ((status == null) ? 0 : status.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        HostRequestDTO other = (HostRequestDTO) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        if (userId == null) {
            if (other.userId != null)
                return false;
        } else if (!userId.equals(other.userId))
            return false;
        if (status == null) {
            if (other.status != null)
                return false;
        } else if (!status.equals(other.status))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "HostRequestDTO [id=" + id + ", userId=" + userId + ", status=" + status + ", requestedAt=" + requestedAt
                + ", reviewedBy=" + reviewedBy + "]";
    }
}
